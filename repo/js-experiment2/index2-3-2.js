// 编写一个函数，在页面上输出1~1000之间所有能同时被3，5，7整除的整数，并要求每行显示6个这样的数
var line = 0;
for (var i = 1; i <= 1000; i++) {
    if (i % 3 == 0 && i % 5 == 0 && i % 7 == 0) {
        document.write(i + " "); // 输出并空格
        line++;
        if (line % 6 == 0) { // 每输出6个数就换行
            document.write("</br>");
        }
    }
}